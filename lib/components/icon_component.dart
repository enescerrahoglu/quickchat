class IconComponent extends StatelessWidget {
  final CustomIconData iconData;
  final CustomIconWeight iconWeight;
  final Color? color;
  final double size;
  const IconComponent({Key? key, required this.iconData, this.iconWeight = CustomIconWeight.solid, this.color, this.size = 20}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    final themeProvider = Provider.of<ThemeProvider>(context);
    return SvgPicture.asset(
      _getIconPath(iconData, iconWeight),
      width: size,
      height: size,
      color: color ?? (themeProvider.isDarkMode ? iconDarkColor : iconLightColor),
    );
  }
}

String _getIconPath(CustomIconData iconData, CustomIconWeight iconWeight) {
  String iconName = describeEnum(iconData).camelcaseToKebabCase();
  String weightPath = describeEnum(iconWeight);

  return "lib/assets/icons/$weightPath/$iconName.svg";
}

